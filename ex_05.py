a = int(input("Введите выручку фирмы = "))
b = int(input("Введите издержки фирмы  = "))
if a > b:
    print("Фирма работает с прибылью.")
    c = int(a - b)
    rent = float(c / a)
    print("Рентабельность составляет = ", rent)
    w = int(input("Введите количество сотрудников фирмы = "))
    d = float(c / w)
    print("Прибыль в расчете на одного сторудника сотавила = ", d)
elif a == b:
    print("Фирма работает в ноль")
else:
    print("Фирма работает в убыток")